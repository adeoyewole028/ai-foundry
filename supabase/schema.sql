create or replace function public.ai_foundry_level_for_xp(total_xp integer)
returns integer as $$
begin
  return case
    when total_xp >= 35000 then 20
    when total_xp >= 20000 then 15
    when total_xp >= 12000 then 10
    when total_xp >= 8500 then 9
    when total_xp >= 6000 then 8
    when total_xp >= 4200 then 7
    when total_xp >= 2800 then 6
    when total_xp >= 1800 then 5
    when total_xp >= 1000 then 4
    when total_xp >= 500 then 3
    when total_xp >= 200 then 2
    else 1
  end;
end;
$$ language plpgsql immutable;

create or replace function public.award_xp_event(
  p_event_type text,
  p_entity_type text,
  p_entity_id text,
  p_xp_amount integer
)
returns boolean as $$
declare
  inserted_event record;
  existing_stats record;
  next_total_xp integer;
  next_streak integer;
  today date := current_date;
begin
  if auth.uid() is null then
    raise exception 'not_authenticated';
  end if;

  if p_xp_amount <= 0 then
    raise exception 'xp_amount_must_be_positive';
  end if;

  insert into public.xp_events (
    user_id,
    event_type,
    entity_type,
    entity_id,
    xp_amount
  )
  values (
    auth.uid(),
    p_event_type,
    p_entity_type,
    p_entity_id,
    p_xp_amount
  )
  on conflict (user_id, event_type, entity_type, entity_id) do nothing
  returning * into inserted_event;

  if inserted_event is null then
    return false;
  end if;

  select *
  into existing_stats
  from public.user_stats
  where user_id = auth.uid()
  for update;

  if existing_stats is null then
    next_total_xp := p_xp_amount;
    next_streak := 1;
  else
    next_total_xp := existing_stats.total_xp + p_xp_amount;
    next_streak := case
      when existing_stats.last_activity_date = today then existing_stats.current_streak
      when existing_stats.last_activity_date = today - 1 then existing_stats.current_streak + 1
      else 1
    end;
  end if;

  insert into public.user_stats (
    user_id,
    total_xp,
    current_level,
    current_streak,
    longest_streak,
    last_activity_date
  )
  values (
    auth.uid(),
    next_total_xp,
    public.ai_foundry_level_for_xp(next_total_xp),
    next_streak,
    next_streak,
    today
  )
  on conflict (user_id) do update set
    total_xp = excluded.total_xp,
    current_level = excluded.current_level,
    current_streak = excluded.current_streak,
    longest_streak = greatest(public.user_stats.longest_streak, excluded.current_streak),
    last_activity_date = excluded.last_activity_date,
    updated_at = now();

  return true;
end;
$$ language plpgsql security definer set search_path = public;

